public class task38 {

    static int square(int n) {
        return n * n;
    }

    static String greet(String name) {
        return "Hello, " + name;
    }

    public static void main(String[] args) {
        System.out.println("Square of 5 : " + square(5));
        System.out.println(greet("World"));
    }
}
