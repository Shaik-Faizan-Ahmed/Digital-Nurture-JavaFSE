public class task37 {

    static int add(int a, int b) {
        return a + b;
    }

    static int multiply(int a, int b) {
        return a * b;
    }

    public static void main(String[] args) {
        System.out.println("Result of add(3, 4)      = " + add(3, 4));
        System.out.println("Result of multiply(3, 4) = " + multiply(3, 4));
    }
}
